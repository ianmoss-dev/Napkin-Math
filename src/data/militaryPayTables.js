export function getBasePay(rank, tis) {
  const brackets = PAY_TABLE[rank];
  if (!brackets) return 0;
  let pay = brackets[0][1];
  for (const [minTis, amount] of brackets) {
    if (tis >= minTis) pay = amount;
    else break;
  }
  return pay;
}
